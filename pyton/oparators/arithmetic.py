"""
    Arithmetic operator are use to perform to arithmetic operation between variable
    1.+
    2.-
    3.*
    4./
    5.%
    6.** (exponent)
    7.// (flow division)
"""
x = 10
y = 20

print("10 + 20")
print(x + y)

print("10 - 20")
print(x - y)

print("10 * 20")
print(x * y)

print("10 / 20")
print(x / y)

print("10 ** 20")
print(x ** y)

print("rimendar of 10 / 20 ")
print(x % y)

print("flow division 10 // 20")
print(x // y)
