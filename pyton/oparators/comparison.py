# Comparison operators are used two compare two values.
# ==, != , < , > , >=, <=

x = 10
y = 20
print("x values: " + str(x) + "\ty values: " + str(y))

print("\n== : check if both values are equal")
print(x == y)

print("\n != : check if both values are not equal")
print(x != y)

print("\n< : less then")
print(x < y)

print("\n> : grater then")
print(x > y)

print("\n<= : less then equal to ")
print(x <= y)

print("\n=> : grater then equal to")
print(x >= y)

